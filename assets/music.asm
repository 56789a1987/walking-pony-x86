	dw 3000 ; PAL 速度

; 乐器指针列表
ft_instrument_list:
	dd ft_inst_0

; 乐器
ft_inst_0:
	db 19
	dd ft_seq_2a03_0
	dd ft_seq_2a03_1
	dd ft_seq_2a03_4

; 序列
ft_seq_2a03_0:
	db 0x01, 0xFF, 0x00, 0x00, 0x0F
ft_seq_2a03_1:
	db 0x01, 0xFF, 0x00, 0x00, 0xF4
ft_seq_2a03_4:
	db 0x01, 0xFF, 0x00, 0x00, 0x01

; DPCM 乐器列表 (音调, 采样指标)
ft_sample_list:

; DPCM 采样列表 (位置, 大小, 岸)
ft_samples:


; 歌曲指针列表
ft_song_list: 
	dd ft_song_0

; 歌曲信息
ft_song_0: 
	dd ft_s0_frames
	db 1	; 帧数
	db 64	; 旋律长度
	db 8	; 速度
	db 150	; 节拍
	db 0	; 内部岸


;
; 用于下列全部歌曲的旋律和帧数据
;

ft_s0_frames: 
	dd ft_s0f0
ft_s0f0:
	dd ft_s0p0c0, ft_s0p0c1, ft_s0p0c1, ft_s0p0c1, ft_s0p0c1
