	db 0x02, 0x2A, 0x00, 0x7F, 0x06, 0x2C, 0x00, 0x7F, 0x04, 0x2C, 0x00, 0x7F, 0x02, 0x82, 0x00, 0x2B, 0x7F, 0x2A, 0x7F, 0x28
